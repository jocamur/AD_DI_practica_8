    public AgregarActualizarPaciente(PacientesController controlador, boolean agregandoPaciente) {
        initComponents();
        this.controlador = controlador;
        this.agregandoPaciente = agregandoPaciente;

        // Si estamos actualizando cargamos toda la informacion
        if (!agregandoPaciente) {
            Titulo.setText("Actualizar paciente");
            buttonAgregar.setText("Actualizar");

            sip.setText(controlador.getPacienteActual().getSip());
            dni.setText(controlador.getPacienteActual().getDni());
            nombre.setText(controlador.getPacienteActual().getNombre());
            apellido1.setText(controlador.getPacienteActual().getApellido1());
            apellido2.setText(controlador.getPacienteActual().getApellido2());
            telefono.setText(controlador.getPacienteActual().getTelefono());
            sexo.setSelectedItem(controlador.getPacienteActual().getSexo());

            // Con un conversor obtenemos la fecha
            SimpleDateFormat conversorFecha = new SimpleDateFormat("yyyy-MM-dd");
            Date fecha = null;

            try {
                fecha = conversorFecha.parse(controlador.getPacienteActual().getFechaNacimiento());
            } catch (ParseException ex) {
                Logger.getLogger(AgregarActualizarCita.class.getName()).log(Level.SEVERE, null, ex);
            }

            LocalDate fechaLocal = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            fechaNacimiento.setDate(fechaLocal);
            localidad.setText(controlador.getPacienteActual().getLocalidad());
            calle.setText(controlador.getPacienteActual().getCalle());
            numero.setText(controlador.getPacienteActual().getNumero());
            puerta.setText(controlador.getPacienteActual().getPuerta());
            piso.setText(controlador.getPacienteActual().getPiso());
            idMedico.setText(controlador.getPacienteActual().getMedicoId());
            idEnfermero.setText(controlador.getPacienteActual().getEnfermeroId());
            idUser.setText(controlador.getPacienteActual().getUserId());
        }
    }

