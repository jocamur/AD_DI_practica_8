    private void buttonAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAgregarMouseClicked
        // Variable para guardar el resultado de la operacion de agregar
        boolean resultado = false;

        if (agregandoPaciente) {
            resultado = controlador.AgregarPaciente(sip.getText(), dni.getText(), nombre.getText(), apellido1.getText(), apellido2.getText(), telefono.getText(), sexo.getSelectedItem().toString(), fechaNacimiento.toString(), localidad.getText(), calle.getText(), numero.getText(), puerta.getText(), piso.getText(), idMedico.getText(), idEnfermero.getText(), idUser.getText());
        } else {
            resultado = controlador.ActualizarPaciente(sip.getText(), dni.getText(), nombre.getText(), apellido1.getText(), apellido2.getText(), telefono.getText(), sexo.getSelectedItem().toString(), fechaNacimiento.toString(), localidad.getText(), calle.getText(), numero.getText(), puerta.getText(), piso.getText(), idMedico.getText(), idEnfermero.getText(), idUser.getText());
        }

        // Si el resultado es verdadero, significa que se agrego con exito, si no, ocurrio un error, mostramos un mensaje en consecuencia
        if (resultado) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Cambios registrados correctamente!", "Alerta", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Error al registrar los cambios!", "Alerta", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_buttonAgregarMouseClicked

