    public AgregarActualizarCita(CitasController controlador, boolean agregandoCita) {
        initComponents();
        this.controlador = controlador;
        this.agregandoCita = agregandoCita;

        // Si estamos actualizando cargamos toda la informacion
        if (!agregandoCita) {
            Titulo.setText("Actualizar cita");
            buttonAgregar.setText("Actualizar");

            // Con un conversor obtenemos la fecha
            SimpleDateFormat conversorFecha = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Date fecha = null;

            try {
                fecha = conversorFecha.parse(controlador.obtenerCitaActual().getFecha());
            } catch (ParseException ex) {
                Logger.getLogger(AgregarActualizarCita.class.getName()).log(Level.SEVERE, null, ex);
            }

            LocalDate fechaLocal = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            LocalTime horaLocal = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalTime();

            fechaCita.timePicker.setTime(horaLocal);
            fechaCita.datePicker.setDate(fechaLocal);
            idMedico.setText(controlador.obtenerCitaActual().getMedicoId());
            idEnfermero.setText(controlador.obtenerCitaActual().getEnfermeroId());
            idPaciente.setText(controlador.obtenerCitaActual().getPacienteId());
        }
    }

