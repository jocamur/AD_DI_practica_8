    private void buttonAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonAgregarMouseClicked
        // Variable para guardar el resultado de la operacion de agregar
        boolean resultado = false;

        if (agregandoCita) {
            resultado = controlador.AgregarCita(fechaCita.toString(), idMedico.getText(), idEnfermero.getText(), idPaciente.getText());
        } else {
            resultado = controlador.ActualizarCita(fechaCita.toString(), idMedico.getText(), idEnfermero.getText(), idPaciente.getText());
        }

        // Si el resultado es verdadero, significa que se agrego con exito, si no, ocurrio un error, mostramos un mensaje en consecuencia
        if (resultado)
            JOptionPane.showMessageDialog(this, "¡Cambios registrados correctamente!", "Alerta", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this, "¡Error al registrar los cambios!", "Alerta", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_buttonAgregarMouseClicked

